<script>
    var basepath = "<?php echo url('/'); ?>";
    $('.modal-call').on('click', function() {
        let from = $(this).data('from');
        $.ajax({
            url: basepath + '/projects_detail',
            type: "GET",
            data: {
                from: from
            },
            success: function(res) {
                $('#modal-title').html(res.header_title);
                $('#modal-show-body').html(res.html_body);
                $('#modal').modal('show');
            }
        });
    });
</script>
